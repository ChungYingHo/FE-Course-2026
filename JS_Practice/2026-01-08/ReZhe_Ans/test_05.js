// Write a JavaScript program that:
// 1. Defines a function named `greet` that takes one parameter.
// 2. The function should output a greeting message using `console.log()`.
// 3. Calls the function with the argument `"Lars"`.

function greet(name){
  console.log (`Welcome to the world,${name}`)
}
//設定函數greet，使用console.log印出'Welcome to the world'
//使用${}樣板字串去將參數傳進來

greet('Lars') //呼叫參數Lars
